    private void kButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kButton1ActionPerformed

        String name = jTextField3.getText();

        if (name.isEmpty()) {
            // JOptionPane.showMessageDialog(this, "Please enter category name", "Warning", JOptionPane.WARNING_MESSAGE);
            jLabel4.setText("Please enter category name");
            jTextField3.setBorder(new MatteBorder(2, 2, 2, 2, Color.RED));

        } else {

            try {
                ResultSet rs = MySQL.search("SELECT * FROM `category` WHERE `name`='" + name + "'");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "Category already exists", "Warning", JOptionPane.WARNING_MESSAGE);

                } else {

                    MySQL.iud("INSERT INTO `category`(`name`) VALUES('" + name + "')");

                    loadCategory();

                    if (pr != null) {
                        this.pr.loadCategories();
                    }

                    jTextField3.setText("");
                    jLabel4.setText("");
                    jTextField3.setBorder(new MatteBorder(0, 0, 2, 0, Color.WHITE));
                    jTextField3.grabFocus();
                    jTextField2.setText("");
                    JOptionPane.showMessageDialog(this, "New category added", "Success", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_kButton1ActionPerformed

