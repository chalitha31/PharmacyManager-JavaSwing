                //Invoice Payment INSERT

                ResultSet rc = MySQL.search("SELECT * FROM `user_registration`  WHERE `id` = '" + SignIn.userId + "'  ");
                rc.next();

                String user_id = rc.getString("user_id");

                ResultSet ru = MySQL.search("SELECT * FROM `user_login`  WHERE `id` = '" + user_id + "' ");
                ru.next();

                String uname = ru.getString("name");

                ResultSet c = MySQL.search("SELECT * FROM `customer`  WHERE `id` = '" + cid + "' ");
                c.next();

                String cname = c.getString("name");

                Vector v = new Vector();
                //invoice Item INSERT & Stock  UPDATE
                for (int i = 0; i < jTable1.getRowCount(); i++) {

                    String sep = jTable1.getValueAt(i, 5).toString();
                    String pname = jTable1.getValueAt(i, 1).toString();
                    String Mfd = jTable1.getValueAt(i, 6).toString();
                    String Exd = jTable1.getValueAt(i, 7).toString();
                    String qty = jTable1.getValueAt(i, 4).toString();
                    String price = jTable1.getValueAt(i, 5).toString();
                    String amount = jTable1.getValueAt(i, 8).toString();
                    String category = jTable1.getValueAt(i, 2).toString();
                    String brand = jTable1.getValueAt(i, 3).toString();

                    ResultSet r1 = MySQL.search("SELECT * FROM `stock` WHERE `selling_price` = '" + sep + "' AND `mfd` = '" + Mfd + "' AND `exd` = '" + Exd + "' ");
                    r1.next();

                    String sid = r1.getString("id");

                    ResultSet rs3 = MySQL.search("SELECT  * FROM `stock` WHERE `stock`.`id` = '" + sid + "'");
                    rs3.next();

                    String availableQty = rs3.getString("quantity");
                    int updatedQty = Integer.parseInt(availableQty) - Integer.parseInt(qty);

                    ResultSet p = MySQL.search("SELECT * FROM `product` WHERE `name` = '" + pname + "' AND `category_id` IN (SELECT `id` FROM `category` WHERE `name` = '" + category + "') "
                            + "AND `brand_id` IN (SELECT `id` FROM `brand` WHERE `name` = '" + brand + "')");
                    p.next();

                    String pid = p.getString("id");

                    MySQL.iud("UPDATE `stock` SET `quantity` = '" + updatedQty + "' WHERE `id` = '" + sid + "' ");
                    String paymentf = df.format(Double.parseDouble(payment));
                    InvoiceReport ir = new InvoiceReport(uname, cname, pname, pid, qty, price, amount, total, paymentType, paymentf, balance);
                    v.add(ir);

                    MySQL.iud("INSERT INTO `invoice_item` (`qty`,`invoice_id`,`stock_id`) VALUES ('" + qty + "','" + id + "','" + sid + "')");
                }

                //   String filePath = "src//report//invoice.jrxml";
                InputStream filePath = Invoice.class.getResourceAsStream("/report/invoice.jrxml");

                JasperReport jr = JasperCompileManager.compileReport(filePath);

                HashMap parameters = new HashMap();
                parameters.put("Parameter1", uniqueId);

                JRBeanCollectionDataSource datasource = new JRBeanCollectionDataSource(v);

                JasperPrint jp = JasperFillManager.fillReport(jr, parameters, datasource);

                JasperViewer.viewReport(jp, false);

                resetField();

