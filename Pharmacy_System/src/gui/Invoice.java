    private void kButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kButton2ActionPerformed

        //        String sid = jLabel2.getText();
        cid = jLabel9.getText();
        //     String sid = jLabel10.getText();
        String pid = jLabel34.getText();
        String exd = jLabel36.getText();
        String pname = jLabel12.getText();
        String brand = jLabel18.getText();
        String category = jLabel14.getText();
        String mfd = jLabel35.getText();
        String qty = jTextField1.getText();
        String sp = jLabel31.getText();
        String age = jLabel19.getText();

        if (pid.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select Product", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (cid.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select Customer", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("[1-9][0-9]*").matcher(qty).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid Quantity", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {

            try {

                ResultSet r = MySQL.search("SELECT * FROM `stock` WHERE `selling_price` = '" + sp + "' AND `product_id` = '" + pid + "' AND `mfd` = '" + mfd + "' AND `exd` = '" + exd + "'");
                r.next();
                String sid = r.getString("id");

                ResultSet rs = MySQL.search("SELECT  * FROM `stock` WHERE `stock`.`id` = '" + sid + "'");
                rs.next();

                String availableQty = rs.getString("quantity");

                if (Integer.parseInt(availableQty) < Integer.parseInt(qty)) {
                    JOptionPane.showMessageDialog(this, "Quantity Out of stock", "Warning", JOptionPane.WARNING_MESSAGE);

                } else {

                    DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

                    boolean isFound = false;
                    int x = -1;

                    for (int i = 0; i < dtm.getRowCount(); i++) {
                        //             String s = jTable1.getValueAt(i, 1).toString();
                        String sep = jTable1.getValueAt(i, 5).toString();
                        //        String prid = jTable1.getValueAt(i, 1).toString();
                        String Mfd = jTable1.getValueAt(i, 6).toString();
                        String Exd = jTable1.getValueAt(i, 7).toString();

                        ResultSet r1 = MySQL.search("SELECT * FROM `stock` WHERE `selling_price` = '" + sep + "' AND `mfd` = '" + Mfd + "' AND `exd` = '" + Exd + "'");
                        r1.next();

                        String s = r1.getString("id");

                        if (s.equals(sid)) {
                            isFound = true;
                            x = i;
                            break;
                        }
                    }

                    //add
                    if (isFound) {

                        int option = JOptionPane.showConfirmDialog(this, "This product is already added, Do you want to update?", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);

                        if (option == JOptionPane.YES_OPTION) {

                            int oldQty = Integer.parseInt(jTable1.getValueAt(x, 5).toString());
                            int finalQty = oldQty + Integer.parseInt(qty);

                            //check stock
                            if (Integer.parseInt(availableQty) < finalQty) {
                                JOptionPane.showMessageDialog(this, "Quantity Out of stock", "Warning", JOptionPane.WARNING_MESSAGE);

                            } else {

                                jTable1.setValueAt(String.valueOf(finalQty), x, 5);

                                double UpdateItemTotal = finalQty * Double.parseDouble(sp);
                                jTable1.setValueAt(String.valueOf(UpdateItemTotal), x, 9);

                                UpdateTotal();

                            }

                            //check stock
                        }

                        resetField();

                    } else {

                        ResultSet rs2 = MySQL.search("SELECT * FROM `product` WHERE `name` = '" + pname + "' AND `brand_id` IN (SELECT `id` FROM `brand` WHERE `name` = '" + brand + "' )");
                        rs2.next();

                        String img_id = rs2.getString("image_id");

                        ResultSet rimg = MySQL.search("SELECT * FROM `product_img` WHERE `id` = '" + img_id + "'");
                        rimg.next();

                        String imager = rimg.getString("image");

                        jTable1.getColumn("p.img").setCellRenderer(new tableRendere());

                        ImageIcon imageicon = new ImageIcon(imager);

                        Image Timage = imageicon.getImage().getScaledInstance(40, 40, Image.SCALE_SMOOTH);

                        JLabel imglabel = new JLabel();

                        imglabel.setIcon(new ImageIcon(Timage));

                        Vector v = new Vector();
                        v.add(imglabel);
                        v.add(pname);
                        v.add(category);
                        //                        v.add(sid);
                        //                        v.add(pid);
                        v.add(brand);

                        v.add(qty);
                        v.add(sp);
                        v.add(mfd);
                        v.add(exd);

                        double itemtotal = Integer.parseInt(qty) * Double.parseDouble(sp);
                        v.add(df.format(itemtotal));

                        dtm.addRow(v);

                        UpdateTotal();

                        resetField();

                        invocount = jTable1.getRowCount();
                        JOptionPane.showMessageDialog(this, "Product Add To the Invoice", "Success", JOptionPane.INFORMATION_MESSAGE);

                    }
                    //add
                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_kButton2ActionPerformed

